package testing

import (
	"testing"

	"github.com/IWillBurn/gost-go-tpm/tpm2"
	"github.com/IWillBurn/gost-go-tpm/tpm2/transport"
	"github.com/stretchr/testify/require"
)

func Test_NVMarshaling_Hashing(t *testing.T) {
	tpm, err := transport.OpenTPM(tpmPath)
	require.NoError(t, err)
	defer tpm.Close()

	testCases := []struct {
		name string
		data [][]byte
		alg  tpm2.TPMAlgID
	}{
		{
			name: "AlgGOST3411_256",
			data: [][]byte{
				{0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37},
				{0x38, 0x39, 0x30, 0x31, 0x32, 0x33, 0x34, 0x35},
				{0x36, 0x37, 0x38, 0x39, 0x30, 0x31, 0x32, 0x33},
				{0x34, 0x35, 0x36, 0x37, 0x38, 0x39, 0x30, 0x31},
				{0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39},
				{0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37},
				{0x38, 0x39, 0x30, 0x31, 0x32, 0x33, 0x34, 0x35},
				{0x36, 0x37, 0x38, 0x39, 0x30, 0x31, 0x32},
			},
			alg: tpm2.TPMAlgGOST3411256,
		},
		{
			name: "AlgGOST3411_512",
			data: [][]byte{
				{0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37},
				{0x38, 0x39, 0x30, 0x31, 0x32, 0x33, 0x34, 0x35},
				{0x36, 0x37, 0x38, 0x39, 0x30, 0x31, 0x32, 0x33},
				{0x34, 0x35, 0x36, 0x37, 0x38, 0x39, 0x30, 0x31},
				{0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39},
				{0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37},
				{0x38, 0x39, 0x30, 0x31, 0x32, 0x33, 0x34, 0x35},
				{0x36, 0x37, 0x38, 0x39, 0x30, 0x31, 0x32},
			},
			alg: tpm2.TPMAlgGOST3411512,
		},
	}

	for _, testCase := range testCases {
		t.Run(testCase.name, func(t *testing.T) {
			digestAtOnce, err := hashSequenceWithChunks(tpm, testCase.data, testCase.alg, false)
			require.NoError(t, err)

			digestWithInterruptions, err := hashSequenceWithChunks(tpm, testCase.data, testCase.alg, true)
			require.NoError(t, err)

			require.Equal(t, digestAtOnce, digestWithInterruptions)
		})
	}
}
